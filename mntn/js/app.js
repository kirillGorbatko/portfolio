window.onload = function () {
	const parallax = document.querySelector('.mainscreen');

	if (parallax) {
		const content = parallax.querySelector('.mainscreen__container');
		const clouds = parallax.querySelector('.images-mainscreen__clouds');
		const mountains = parallax.querySelector('.images-mainscreen__mountains');
		const man = parallax.querySelector('.images-mainscreen__man');

		// Коэффициенты
		const forClouds = 40;
		const forMountains = 20;
		const forMan = 10;

		// Скорость анимации
		const speed = 0.05;

		let positionX = 0, positionY = 0;
		let coordXpercent = 0, coordYpercent = 0;

		function setMouseParallaxStyle() {
			const distX = coordXpercent - positionX;
			const distY = coordYpercent - positionY;

			positionX = positionX + (distX * speed);
			positionY = positionY + (distY * speed);

			clouds.style.cssText = `transform: translate(${positionX / forClouds}%,${positionY / forClouds}%)`;
			mountains.style.cssText = `transform: translate(${positionX / forMountains}%,${positionY / forMountains}%)`;
			man.style.cssText = `transform: translate(${positionX / forMan}%,${positionY / forMan}%)`;

			requestAnimationFrame(setMouseParallaxStyle);
		}
		setMouseParallaxStyle();

		parallax.addEventListener("mousemove", function (e) {
			const parallaxWidth = parallax.offsetWidth;
			const parallaxHeight = parallax.offsetHeight;

			const coordX = e.pageX - parallaxWidth / 2;
			const coordY = e.pageY - parallaxHeight / 2;

			coordXpercent = coordX / parallaxWidth * 100;
			coordYpercent = coordY / parallaxHeight * 100;
		});

		let thresHoldSets = [];
		for(let i = 0; i <= 1.0; i += 0.005) {
			thresHoldSets.push(i);
		}
		const callback = function (entries, observer) {
			const scrollTopPercent = window.pageYOffset / parallax.offsetHeight * 100;
			setParallaxItemsStyle(scrollTopPercent);
		};
		const observer = new IntersectionObserver(callback, {
			threshold: thresHoldSets
		});

		observer.observe(document.querySelector('.content'));

		function setParallaxItemsStyle(scrollTopPercent) {
			content.style.cssText = `transform: translate(0%,-${scrollTopPercent / 9}%);`;
			mountains.parentElement.style.cssText = `transform: translate(0%,-${scrollTopPercent / 6}%);`;
			man.parentElement.style.cssText = `transform: translate(0%,-${scrollTopPercent / 3}%);`;
		}
	}
}
